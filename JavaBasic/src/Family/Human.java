package Family;


public class Human {
    String name;
    boolean isMale;
    String nameOfMother;
    String nameOfFather;


    public Human(String name, boolean isMale, String nameOfMother, String nameOfFather) {
        this.name = name;
        this.isMale = isMale;
        this.nameOfMother = nameOfMother;
        this.nameOfFather = nameOfFather;
    }
}

